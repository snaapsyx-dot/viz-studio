// ===== CUSTOM CURSOR =====
function initCursor() {
  const c = document.getElementById('cursor');
  const d = document.getElementById('cursorDot');
  let cx = 0, cy = 0, dx = 0, dy = 0;

  document.addEventListener('mousemove', e => { dx = e.clientX; dy = e.clientY; });

  (function loop() {
    cx += (dx - cx) * 0.12;
    cy += (dy - cy) * 0.12;
    c.style.left = cx + 'px';
    c.style.top = cy + 'px';
    d.style.left = dx + 'px';
    d.style.top = dy + 'px';
    requestAnimationFrame(loop);
  })();

  refreshCursorTargets();
}

function refreshCursorTargets() {
  const c = document.getElementById('cursor');
  const targets = 'a, button, .work-card, .client-cell, .sp-item, .reel-wrap, .sw-pill, .process-card, .media-dot';
  document.querySelectorAll(targets).forEach(el => {
    el.removeEventListener('mouseenter', cursorOn);
    el.removeEventListener('mouseleave', cursorOff);
    el.addEventListener('mouseenter', cursorOn);
    el.addEventListener('mouseleave', cursorOff);
  });
}

function cursorOn() { document.getElementById('cursor').classList.add('hover'); }
function cursorOff() { document.getElementById('cursor').classList.remove('hover'); }

// ===== LENIS SMOOTH SCROLL =====
let lenisInstance;
function initLenis() {
  if (typeof Lenis === 'undefined') return;
  lenisInstance = new Lenis({
    duration: 1.2,
    easing: t => Math.min(1, 1.001 - Math.pow(2, -10 * t)),
    touchMultiplier: 2,
    infinite: false,
  });

  function raf(time) {
    lenisInstance.raf(time);
    requestAnimationFrame(raf);
  }
  requestAnimationFrame(raf);
}

// ===== HERO GSAP ANIMATIONS =====
function initHeroAnimations() {
  if (typeof gsap === 'undefined') return;

  const tl = gsap.timeline({ delay: 0.8 });

  tl.to('.hero-eyebrow span', {
    y: 0, duration: 0.8, ease: 'power3.out'
  })
  .to('.hero-h1 .line span', {
    y: 0, duration: 1, stagger: 0.12, ease: 'power3.out'
  }, '-=0.4')
  .to('.hero-desc', {
    opacity: 1, y: 0, duration: 0.8, ease: 'power3.out'
  }, '-=0.5')
  .to('.hero-ctas', {
    opacity: 1, y: 0, duration: 0.8, ease: 'power3.out'
  }, '-=0.5')
  .to('.cube-wrap', {
    opacity: 1, scale: 1, rotation: 0, filter: 'blur(0px)',
    duration: 1.4, ease: 'power3.out',
    onStart: () => {
      document.querySelector('.cube-glow-ring').style.opacity = '1';
    }
  }, '-=0.8');

  // Parallax cube on scroll
  if (typeof ScrollTrigger !== 'undefined') {
    gsap.to('.cube-scene', {
      y: -120,
      scrollTrigger: {
        trigger: '.hero',
        start: 'top top',
        end: 'bottom top',
        scrub: 1,
      }
    });

    gsap.to('.hero-content', {
      y: -60,
      opacity: 0.3,
      scrollTrigger: {
        trigger: '.hero',
        start: '60% top',
        end: 'bottom top',
        scrub: 1,
      }
    });
  }
}

// ===== REVEAL ON SCROLL =====
let revealObs;
function initReveal() {
  revealObs = new IntersectionObserver(entries => {
    entries.forEach(e => { if (e.isIntersecting) e.target.classList.add('vis'); });
  }, { threshold: 0.08, rootMargin: '0px 0px -30px 0px' });

  document.querySelectorAll('.reveal').forEach(el => revealObs.observe(el));
}

// ===== STAT COUNTER =====
function initStats() {
  const bar = document.querySelector('.stats-bar');
  if (!bar) return;

  const obs = new IntersectionObserver(([e]) => {
    if (!e.isIntersecting) return;
    document.querySelectorAll('.stat-num[data-target]').forEach(el => {
      const target = +el.dataset.target;
      const suffix = el.innerHTML.replace(/^[\d]+/, '');
      let cur = 0;
      const step = Math.max(1, Math.ceil(target / 50));
      const iv = setInterval(() => {
        cur += step;
        if (cur >= target) { cur = target; clearInterval(iv); }
        el.innerHTML = cur + suffix;
      }, 25);
    });
    obs.disconnect();
  }, { threshold: 0.5 });

  obs.observe(bar);
}

