async function saveEditClient(id) {
  const data = {
    name: document.getElementById('ec-name').value,
    description: document.getElementById('ec-desc').value,
    tag: document.getElementById('ec-tag').value,
    subscribers: document.getElementById('ec-subs').value,
    avatar_url: document.getElementById('ec-avatar-url').value,
    initials: document.getElementById('ec-initials').value,
    url: document.getElementById('ec-url').value,
    sort_order: +document.getElementById('ec-sort').value || 0
  };

  if (!data.name) { showToast('Name is required'); return; }

  const url = id ? `/api/admin/clients/${id}` : '/api/admin/clients';
  const method = id ? 'PUT' : 'POST';

  try {
    const res = await fetch(url, { method, headers: { 'Content-Type': 'application/json' }, body: JSON.stringify(data) });
    if (res.ok) {
      closeEditModal();
      await reloadAndRender();
      showToast(id ? 'Client updated' : 'Client created');
    } else { showToast('Failed to save'); }
  } catch { showToast('Server error'); }
}
window.saveEditClient = saveEditClient;

async function deleteEditClient(id) {
  if (!confirm('Delete this client?')) return;
  try {
    const res = await fetch(`/api/admin/clients/${id}`, { method: 'DELETE' });
    if (res.ok) {
      closeEditModal();
      await reloadAndRender();
      showToast('Client deleted');
    }
  } catch { showToast('Server error'); }
}
window.deleteEditClient = deleteEditClient;

