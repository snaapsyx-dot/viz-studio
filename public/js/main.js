let currentLang = localStorage.getItem('viz_lang') || 'en';

function t(key) {
  return translations[currentLang]?.[key] || translations.en[key] || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val) el.innerHTML = val;
  });
  // Update placeholder
  const emailInput = document.getElementById('emailInput');
  if (emailInput) emailInput.placeholder = t('cta_placeholder');
  // Update html lang
  document.documentElement.lang = currentLang;
  // Update lang toggle button
  const btn = document.getElementById('langToggle');
  if (btn) btn.textContent = currentLang === 'en' ? 'RU' : 'EN';
  // Re-render clients with correct language
  renderClients();
}

function toggleLang() {
  currentLang = currentLang === 'en' ? 'ru' : 'en';
  localStorage.setItem('viz_lang', currentLang);
  applyTranslations();
}

window.toggleLang = toggleLang;

