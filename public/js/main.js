function initCubeFollow() {
  const scene = document.querySelector('.cube-scene');
  const cubeObj = document.querySelector('.cube-obj');
  if (!scene || !cubeObj) return;

  let clicks = 0;
  let timer;
  let following = false;
  let mouseX = window.innerWidth / 2;
  let mouseY = window.innerHeight / 2;

  scene.addEventListener('click', () => {
    clicks++;
    clearTimeout(timer);
    timer = setTimeout(() => { clicks = 0; }, 600);

    if (clicks >= 2) {
      clicks = 0;
      following = !following;
      cubeObj.classList.toggle('follow-mode', following);
      scene.classList.toggle('follow-active', following);

      if (!following) {
        cubeObj.style.transform = '';
      }
    }
  });

  document.addEventListener('mousemove', e => {
    mouseX = e.clientX;
    mouseY = e.clientY;
  });

  (function animate() {
    if (following) {
      const rect = scene.getBoundingClientRect();
      const cx = rect.left + rect.width / 2;
      const cy = rect.top + rect.height / 2;
      const dx = (mouseX - cx) / (rect.width / 2);
      const dy = (mouseY - cy) / (rect.height / 2);
      const rotY = dx * 40;
      const rotX = -dy * 30;
      cubeObj.style.transform = `rotateX(${rotX}deg) rotateY(${rotY}deg)`;
    }
    requestAnimationFrame(animate);
  })();
}

